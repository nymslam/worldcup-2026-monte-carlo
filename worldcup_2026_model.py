# Validates the model by replaying the 2018 and 2022 World Cups: it fits the
# model using ONLY data before each kickoff, predicts the actual matches (which
# already live in results.csv), and scores those probabilities with proper
# metrics against two baselines. This is what tells you whether the model is
# any good -- and whether a hyperparameter like FRIENDLY_WEIGHT is well chosen.
#
# >>> In Colab: run the Part 1 cell first, then this cell, then: main() <<<
#
# NOTE: the bracket/stage-level calibration (P(reach QF) etc.) is a separate
# add-on because it needs the historical group draws transcribed; this file
