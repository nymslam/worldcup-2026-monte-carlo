HOST_ADVANTAGE_SCALE = 0.40

# -----------------------------------------------------------------------------
# RATINGS LOOKUP -- convert the fitted arrays to dicts and fill any team that
# the model never saw (debutant / name mismatch) with a weak fallback so the
# engine never crashes; it prints the list so you can fix names.
# -----------------------------------------------------------------------------
def build_engine_ratings(params, all_teams, hosts):
    idx = params["idx"]
    att = {t: params["attack"][i] for t, i in idx.items()}
    dff = {t: params["defense"][i] for t, i in idx.items()}
    # weak fallback = 10th percentile of fitted strength, split evenly
    a_fallback = np.percentile(list(att.values()), 10)
    d_fallback = np.percentile(list(dff.values()), 10)
    missing = [t for t in all_teams if t not in idx]
    for t in missing:
        att[t], dff[t] = a_fallback, d_fallback
    if missing:
        print("WARNING -- not found in fitted ratings (using weak fallback):")
        print("  ", missing)
        print("  Fix these via NAME_MAP / the group dict, then re-run.\n")
    return {"att": att, "def": dff, "home": params["home"], "rho": params["rho"],
            "hosts": set(hosts), "host_adv": params["home"] * HOST_ADVANTAGE_SCALE}

def team_lambdas(eng, t1, t2):
    """Expected goals for t1 and t2, applying host advantage where it applies."""
    adv = eng["host_adv"]
    adv1 = adv if (t1 in eng["hosts"] and t2 not in eng["hosts"]) else 0.0
    adv2 = adv if (t2 in eng["hosts"] and t1 not in eng["hosts"]) else 0.0
    lam1 = np.exp(eng["att"][t1] - eng["def"][t2] + adv1)
    lam2 = np.exp(eng["att"][t2] - eng["def"][t1] + adv2)
    return lam1, lam2

# -----------------------------------------------------------------------------
# SCORELINE SAMPLING -- precompute each fixture's full Dixon-Coles distribution
# once (memoized), then sampling a result is a single searchsorted.
# -----------------------------------------------------------------------------
def get_dist(eng, t1, t2, cache):
    key = (t1, t2)
    if key in cache:
        return cache[key]
    lam1, lam2 = team_lambdas(eng, t1, t2)
    M = scoreline_matrix(lam1, lam2, eng["rho"])     # axis0 = t1 goals, axis1 = t2 goals
    flat = M.ravel()
    cumsum = np.cumsum(flat)
    g1 = np.repeat(np.arange(M.shape[0]), M.shape[1])
    g2 = np.tile(np.arange(M.shape[1]), M.shape[0])
    cache[key] = (cumsum, g1, g2)
    return cache[key]

def sample_match(eng, t1, t2, cache):
    cumsum, g1, g2 = get_dist(eng, t1, t2, cache)
    i = np.searchsorted(cumsum, np.random.random() * cumsum[-1])
    return int(g1[i]), int(g2[i])

def play_knockout(eng, t1, t2, cache):
    g1, g2 = sample_match(eng, t1, t2, cache)
    if g1 != g2:
        return t1 if g1 > g2 else t2
    lam1, lam2 = team_lambdas(eng, t1, t2)            # extra time
    e1 = np.random.poisson(lam1 * ET_FRACTION)
    e2 = np.random.poisson(lam2 * ET_FRACTION)
    if e1 != e2:
        return t1 if e1 > e2 else t2
    return t1 if np.random.random() < 0.5 else t2     # penalties ~ coin flip

# -----------------------------------------------------------------------------
# GROUP STAGE + THIRD-PLACE RANKING
# Tiebreakers implemented: points, goal difference, goals for, then random
# ("drawing of lots"). Head-to-head and fair-play are omitted (documented).
# -----------------------------------------------------------------------------
def _key(rec):  # rec = (points, goal_diff, goals_for)
    return (rec[0], rec[1], rec[2], np.random.random())

def simulate_group(eng, teams, cache):
    pts = {t: 0 for t in teams}; gf = {t: 0 for t in teams}; ga = {t: 0 for t in teams}
    for i in range(len(teams)):
        for j in range(i + 1, len(teams)):
            t1, t2 = teams[i], teams[j]
            g1, g2 = sample_match(eng, t1, t2, cache)
            gf[t1] += g1; ga[t1] += g2; gf[t2] += g2; ga[t2] += g1
            if g1 > g2:   pts[t1] += 3
            elif g2 > g1: pts[t2] += 3
            else:         pts[t1] += 1; pts[t2] += 1
    rec = {t: (pts[t], gf[t] - ga[t], gf[t]) for t in teams}
    standings = sorted(teams, key=lambda t: _key(rec[t]), reverse=True)
    return standings, rec

# -----------------------------------------------------------------------------
# BRACKET -- seed the 32 qualifiers (winners > runners-up > thirds, then by
# record) and place them in a standard single-elimination bracket.
# NOTE: this is a strength-seeded approximation of FIFA's fixed positional
# template + 495-combination third-place matrix. It gets the bracket TOPOLOGY
# and seeding spirit right; to reproduce FIFA's exact R32 pairings, replace
# build_bracket() with the official mapping. The effect on title/stage
# probabilities is second-order (the strongest teams advance regardless of
# which specific slot they fill).
# -----------------------------------------------------------------------------
def _bracket_seed_positions(n):
    order = [0]
    while len(order) < n:
        size = len(order) * 2
        order = [x for pair in ((s, size - 1 - s) for s in order) for x in pair]
    return order

def build_bracket(qualifiers):
    # qualifiers: list of (team, tier, rec) ; tier 0=winner,1=runner-up,2=third
    seeded = sorted(qualifiers, key=lambda q: (q[1], -q[2][0], -q[2][1], -q[2][2]))
    teams_by_seed = [q[0] for q in seeded]
    return [teams_by_seed[p] for p in _bracket_seed_positions(len(teams_by_seed))]

# -----------------------------------------------------------------------------
# ONE TOURNAMENT
# -----------------------------------------------------------------------------
def simulate_tournament(eng, groups, cache):
    reached = {t: 0 for grp in groups.values() for t in grp}   # 0 = out in group
    winners, runners, thirds = [], [], []
    for g, teams in groups.items():
        standings, rec = simulate_group(eng, teams, cache)
        winners.append((standings[0], 0, rec[standings[0]]))
        runners.append((standings[1], 1, rec[standings[1]]))
        thirds.append((standings[2], 2, rec[standings[2]]))

    best_thirds = sorted(thirds, key=lambda q: _key(q[2]), reverse=True)[:8]
    qualifiers = winners + runners + best_thirds
    for t, _, _ in qualifiers:
        reached[t] = 1                                          # reached R32

    bracket = build_bracket(qualifiers)
    round_teams, reward = bracket, 2
    while len(round_teams) > 1:
        nxt = []
        for k in range(0, len(round_teams), 2):
            w = play_knockout(eng, round_teams[k], round_teams[k + 1], cache)
            reached[w] = reward
            nxt.append(w)
        round_teams, reward = nxt, reward + 1
    return reached  # values: 0 grp, 1 R32, 2 R16, 3 QF, 4 SF, 5 Final, 6 Champion

# -----------------------------------------------------------------------------
# MONTE CARLO
# -----------------------------------------------------------------------------
def run_monte_carlo(params, groups=GROUPS_2026, hosts=DEFAULT_HOSTS, n=20000, seed=0):
    np.random.seed(seed)
    all_teams = [t for grp in groups.values() for t in grp]
    eng = build_engine_ratings(params, all_teams, hosts)
    cache = {}
    counts = {t: np.zeros(7, dtype=np.int64) for t in all_teams}  # index = stage reached
    for _ in range(n):
        reached = simulate_tournament(eng, groups, cache)
        for t, r in reached.items():
            counts[t][r] += 1

    rows = []
    for t in all_teams:
        c = counts[t]
        # P(reach AT LEAST stage s) = cumulative from the top
        p_r32   = c[1:].sum() / n
        p_r16   = c[2:].sum() / n
        p_qf    = c[3:].sum() / n
        p_sf    = c[4:].sum() / n
        p_final = c[5:].sum() / n
        p_win   = c[6]        / n
        rows.append((t, p_win, p_final, p_sf, p_qf, p_r16, p_r32))
    df = pd.DataFrame(rows, columns=["team", "win", "final", "semi",
                                     "quarter", "r16", "qualify"])
    return df.sort_values("win", ascending=False).reset_index(drop=True)

