def backtest_year(year, n=10000):
    params, _ = fit_as_of(KICKOFF[year])
    groups = GROUPS_2018 if year == 2018 else GROUPS_2022
    actual = ACTUAL_2018 if year == 2018 else ACTUAL_2022
    pred = run_mc_historical(params, groups, HOST[year], n=n)

    champ = max(actual, key=actual.get)
    rank = pred.sort_values("Champion", ascending=False).reset_index(drop=True)
    champ_rank = rank.index[rank["team"] == champ][0] + 1
    champ_prob = float(rank.loc[rank["team"] == champ, "Champion"].iloc[0])
    print(f"\n{year}: actual champion {champ} was ranked #{champ_rank} "
          f"in predicted title probability ({champ_prob:.1%}).")
    print(f"  predicted top 4: "
          f"{', '.join(rank['team'].head(4) + ' ' + (rank['Champion'].head(4)*100).round(1).astype(str) + '%')}")

    # collect pooled (predicted, actual) over the 5 stage thresholds
    points = []
    for _, row in pred.iterrows():
        a = actual.get(row["team"], 0)
        for k, name in enumerate(STAGE_NAMES, start=1):
            points.append((row[name], 1 if a >= k else 0))
    return points

# -----------------------------------------------------------------------------
# CALIBRATION over the pooled (predicted, actual) points from both years.
# -----------------------------------------------------------------------------
def calibration_report(points, bins=5):
    p = np.array([x[0] for x in points]); y = np.array([x[1] for x in points])
    brier = np.mean((p - y) ** 2)
    edges = np.linspace(0, 1, bins + 1)
    print("\n--- engine calibration (pooled 2018 + 2022, team x stage events) ---")
    print(f"{'predicted band':>16} | {'n':>4} | {'mean pred':>9} | {'actual freq':>11}")
    ece = 0.0
    for b in range(bins):
        hi = p <= edges[b+1] if b == bins-1 else p < edges[b+1]
        m = (p >= edges[b]) & hi
        if m.sum():
            ece += (m.sum()/len(p)) * abs(p[m].mean() - y[m].mean())
            print(f"  {edges[b]:.1f}-{edges[b+1]:.1f}      | {m.sum():>4} | "
                  f"{p[m].mean():>9.2f} | {y[m].mean():>11.2f}")
    print(f"\nBrier score: {brier:.4f}   ECE: {ece:.3f}   (both lower = better)")
    print("Well calibrated => 'mean pred' ~ 'actual freq' in every band.")

def backtest_stages(n=10000):
    pts = []
    for yr in (2018, 2022):
        pts += backtest_year(yr, n=n)
    calibration_report(pts)



