import numpy as np
import pandas as pd
from scipy.optimize import minimize
from scipy.special import gammaln
from scipy.stats import poisson

# -----------------------------------------------------------------------------
# CONFIG -- these are your hyperparameters. The starting values are sensible;
# in Part 2 you will TUNE them by backtesting rather than trust them blindly.
# -----------------------------------------------------------------------------
RESULTS_PATH    = "results.csv"   # path to the Kaggle file in your Colab session
TRAIN_YEARS_BACK = 12             # ignore matches older than this (squad drift)
HALF_LIFE_DAYS  = 900             # time-decay half-life (~2.5 years)
FRIENDLY_WEIGHT = 0.20            # friendlies kept as LOW-weight cross-confederation bridges
GOAL_CAP        = 5               # cap blowout scorelines so minnow-thrashings don't inflate attack
KAPPA           = 8.0             # strength of the Elo prior (higher = more shrinkage)
SIGMA_SCALE     = 0.55            # spread of Elo-implied strength on the log-goal scale
MAX_GOALS       = 10              # truncation for the scoreline matrix in prediction
RHO_BOUNDS      = (-0.2, 0.2)     # Dixon-Coles low-score dependence bound
HFA_ELO         = 65.0           # home-field advantage in Elo points (non-neutral only)
ELO_K           = 30.0           # base Elo K-factor
LN2             = np.log(2.0)

# -----------------------------------------------------------------------------
# 1. TEAM-NAME NORMALIZATION
#    The dataset splits some nations across historical / alternate spellings.
#    This is a STARTER map -- expand it as you spot duplicates in your data
#    (run df.home_team.unique() and eyeball it).
# -----------------------------------------------------------------------------
NAME_MAP = {
    "United States": "USA",
    "Republic of Ireland": "Ireland",
    "China PR": "China",
    "Korea Republic": "South Korea",
    "Korea DPR": "North Korea",
    "Czechoslovakia": "Czech Republic",   # debatable; merge or drop per taste
    "West Germany": "Germany",
    "Yugoslavia": "Serbia",               # debatable historical merge
    "Zaire": "DR Congo",
    "Cape Verde Islands": "Cape Verde",
}

def normalize_names(df):
    df = df.copy()
    for col in ("home_team", "away_team"):
        df[col] = df[col].replace(NAME_MAP)
    return df

# -----------------------------------------------------------------------------
# 2. COMPETITION IMPORTANCE TIERS (phi). Friendlies are kept at a LOW weight
#    because they are the main matches that BRIDGE confederations -- dropping
#    them leaves the model unable to calibrate (e.g.) Asia vs Europe.
#    Order matters: check 'qualification' before the finals keywords.
# -----------------------------------------------------------------------------
def competition_weight(tournament: str) -> float:
    t = tournament.lower()
    if "friendly" in t:
        return FRIENDLY_WEIGHT
    if "qualification" in t or "qualifier" in t:
        return 0.70
    if "nations league" in t:
        return 0.60
    if t == "fifa world cup" or "world cup" == t:
        return 1.00
    # continental finals tournaments
    finals_kw = ["uefa euro", "copa am", "africa cup", "african cup",
                 "afc asian cup", "gold cup", "concacaf championship",
                 "confederations cup", "copa america"]
    if any(k in t for k in finals_kw):
        return 0.90
    # any other competitive match (regional cups, etc.)
    return 0.50

# -----------------------------------------------------------------------------
# 3. DATA LOADING + COMPETITIVE FILTER + WEIGHTS
# -----------------------------------------------------------------------------
def load_and_prepare(path=RESULTS_PATH, as_of=None):
    df = pd.read_csv(path, parse_dates=["date"])
    df = normalize_names(df)

    if as_of is None:
        as_of = df["date"].max()          # default: predict "today"
    as_of = pd.Timestamp(as_of)

    # STRICT cutoff -- nothing on or after the prediction date may be used.
    df = df[df["date"] < as_of].copy()
    df = df[df["date"] >= as_of - pd.Timedelta(days=365 * TRAIN_YEARS_BACK)]

    df["phi"] = df["tournament"].apply(competition_weight)
    df = df[df["phi"] > 0].copy()          # keep everything with a positive weight

    # time decay anchored to the PREDICTION date (not "now")
    age_days = (as_of - df["date"]).dt.days.to_numpy()
    df["w"] = df["phi"].to_numpy() * np.exp(-LN2 * age_days / HALF_LIFE_DAYS)

    # normalize mean weight to 1 so effective N and prior strength are comparable
    df["w"] = df["w"] * len(df) / df["w"].sum()

    df = df.dropna(subset=["home_score", "away_score"])
    df["home_score"] = df["home_score"].astype(int)
    df["away_score"] = df["away_score"].astype(int)
    # cap blowouts: a 7-0 over a minnow shouldn't inflate attack more than a 5-0
    df["home_score"] = df["home_score"].clip(upper=GOAL_CAP)
    df["away_score"] = df["away_score"].clip(upper=GOAL_CAP)
    return df.reset_index(drop=True), as_of

# -----------------------------------------------------------------------------
# 4. ELO -- computed on the same competitive set. Returns ratings as of `as_of`.
#    Used as (a) the prior target for Dixon-Coles and (b) a backtest baseline.
# -----------------------------------------------------------------------------
def compute_elo(df):
    ratings = {}
    def get(t): return ratings.get(t, 1500.0)
    for r in df.sort_values("date").itertuples(index=False):
        rh, ra = get(r.home_team), get(r.away_team)
        hfa = 0.0 if r.neutral else HFA_ELO
        exp_h = 1.0 / (1.0 + 10 ** ((ra - rh - hfa) / 400.0))
        gd = abs(r.home_score - r.away_score)
        g = 1.0 if gd <= 1 else (1.5 if gd == 2 else (1.75 + (gd - 3) / 8.0))
        if r.home_score > r.away_score:   s = 1.0
        elif r.home_score < r.away_score: s = 0.0
        else:                             s = 0.5
        delta = ELO_K * r.phi * g * (s - exp_h)   # importance-weighted: friendlies nudge gently
        ratings[r.home_team] = rh + delta
        ratings[r.away_team] = ra - delta
    return ratings

# -----------------------------------------------------------------------------
# 5. WEIGHTED DIXON-COLES with Elo-prior penalty
# -----------------------------------------------------------------------------
def _tau_log(x, y, lam, mu, rho):
    """Vectorized log of the Dixon-Coles low-score correction."""
    t = np.ones_like(lam)
    m00 = (x == 0) & (y == 0); t[m00] = 1 - lam[m00] * mu[m00] * rho
    m01 = (x == 0) & (y == 1); t[m01] = 1 + lam[m01] * rho
    m10 = (x == 1) & (y == 0); t[m10] = 1 + mu[m10] * rho
    m11 = (x == 1) & (y == 1); t[m11] = 1 - rho
    return np.log(np.clip(t, 1e-10, None))

def fit_dixon_coles(df, elo_ratings, kappa=KAPPA, sigma_scale=SIGMA_SCALE):
    teams = sorted(set(df["home_team"]) | set(df["away_team"]))
    idx = {t: i for i, t in enumerate(teams)}
    n = len(teams)

    h_idx = df["home_team"].map(idx).to_numpy()
    a_idx = df["away_team"].map(idx).to_numpy()
    x = df["home_score"].to_numpy()
    y = df["away_score"].to_numpy()
    not_neutral = (~df["neutral"].astype(bool)).to_numpy().astype(float)
    w = df["w"].to_numpy()

    gx, gy = gammaln(x + 1), gammaln(y + 1)   # precompute log factorials

    # Elo prior target on the log-goal scale (centered z-scores of Elo)
    elo_vec = np.array([elo_ratings.get(t, 1500.0) for t in teams])
    z = (elo_vec - elo_vec.mean()) / (elo_vec.std() + 1e-9)
    sigma_target = z * sigma_scale            # mean ~0 -> a prior on RELATIVE strength

    def neg_loglik(params):
        a = params[:n]
        a = a - a.mean()                      # gauge fix: mean(attack)=0
        d = params[n:2 * n]
        h, rho = params[2 * n], params[2 * n + 1]

        log_lam = a[h_idx] - d[a_idx] + h * not_neutral
        log_mu  = a[a_idx] - d[h_idx]
        lam, mu = np.exp(log_lam), np.exp(log_mu)

        ll_h = x * log_lam - lam - gx
        ll_a = y * log_mu  - mu - gy
        ll   = w * (_tau_log(x, y, lam, mu, rho) + ll_h + ll_a)

        sigma = a + d
        sigma_dev = sigma - sigma.mean()
        penalty = kappa * np.sum((sigma_dev - sigma_target) ** 2)
        return -ll.sum() + penalty

    x0 = np.concatenate([np.zeros(n), np.zeros(n), [0.25, 0.0]])
    bounds = [(None, None)] * (2 * n) + [(-0.5, 1.0), RHO_BOUNDS]
    res = minimize(neg_loglik, x0, method="L-BFGS-B", bounds=bounds,
                   options={"maxiter": 400, "maxfun": 60000})

    a = res.x[:n]; a = a - a.mean()
    d = res.x[n:2 * n]
    h, rho = res.x[2 * n], res.x[2 * n + 1]
    ratings = pd.DataFrame({"team": teams, "attack": a, "defense": d,
                            "strength": a + d, "elo": elo_vec})
    ratings = ratings.sort_values("strength", ascending=False).reset_index(drop=True)
    params = {"idx": idx, "attack": a, "defense": d, "home": h, "rho": rho,
              "converged": res.success}
    return ratings, params

# -----------------------------------------------------------------------------
# 6. MATCH PREDICTOR
# -----------------------------------------------------------------------------
def expected_goals(params, home, away, neutral=True):
    idx, a, d = params["idx"], params["attack"], params["defense"]
    hi, ai = idx[home], idx[away]
    h = 0.0 if neutral else params["home"]
    lam = np.exp(a[hi] - d[ai] + h)
    mu  = np.exp(a[ai] - d[hi])
    return lam, mu

def scoreline_matrix(lam, mu, rho, max_goals=MAX_GOALS):
    gi = np.arange(max_goals + 1)
    M = np.outer(poisson.pmf(gi, lam), poisson.pmf(gi, mu))
    M[0, 0] *= 1 - lam * mu * rho
    M[0, 1] *= 1 + lam * rho
    M[1, 0] *= 1 + mu * rho
    M[1, 1] *= 1 - rho
    M = np.clip(M, 0, None)
    return M / M.sum()

def predict_match(params, home, away, neutral=True):
    lam, mu = expected_goals(params, home, away, neutral)
    M = scoreline_matrix(lam, mu, params["rho"])
    p_home = np.tril(M, -1).sum()   # home goals > away goals
    p_draw = np.trace(M)
    p_away = np.triu(M, 1).sum()
    i, j = np.unravel_index(M.argmax(), M.shape)
    return {"p_home": p_home, "p_draw": p_draw, "p_away": p_away,
            "exp_home_goals": lam, "exp_away_goals": mu,
            "most_likely_score": (int(i), int(j))}

# -----------------------------------------------------------------------------
# 7. SCORING HELPERS (for Part 2 backtesting; defined here so they live with
#    the model). RPS is the headline metric for ordinal 1X2 outcomes.
# -----------------------------------------------------------------------------
def rps(p_home, p_draw, p_away, outcome):
    """outcome in {'H','D','A'}. Order: H > D > A. Lower is better."""
    p = np.array([p_home, p_draw, p_away])
    e = {"H": [1, 0, 0], "D": [0, 1, 0], "A": [0, 0, 1]}[outcome]
    cp, ce = np.cumsum(p), np.cumsum(e)
    return 0.5 * np.sum((cp[:-1] - ce[:-1]) ** 2)

def log_loss_1x2(p_home, p_draw, p_away, outcome):
    p = {"H": p_home, "D": p_draw, "A": p_away}[outcome]
    return -np.log(max(p, 1e-12))

