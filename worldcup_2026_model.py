# -----------------------------------------------------------------------------
# 32-team tournament (top 2 advance, no thirds) -- reuses Part 2's primitives.
# -----------------------------------------------------------------------------
def _simulate_32(eng, groups, cache):
    reached = {t: 0 for g in groups.values() for t in g}
    winners, runners = [], []
    for g, teams in groups.items():
        standings, rec = simulate_group(eng, teams, cache)
        winners.append((standings[0], 0, rec[standings[0]]))
        runners.append((standings[1], 1, rec[standings[1]]))
    qualifiers = winners + runners            # 16 teams, no third-place slots
    for t, _, _ in qualifiers:
        reached[t] = 1
    bracket, reward = build_bracket(qualifiers), 2
    while len(bracket) > 1:
        nxt = []
        for k in range(0, len(bracket), 2):
            w = play_knockout(eng, bracket[k], bracket[k + 1], cache)
            reached[w] = reward; nxt.append(w)
        bracket, reward = nxt, reward + 1
    return reached                            # values 0..5

def run_mc_historical(params, groups, host, n=10000, seed=0):
    np.random.seed(seed)
    all_teams = [t for g in groups.values() for t in g]
    eng = build_engine_ratings(params, all_teams, {host})
    cache = {}
    counts = {t: np.zeros(6, dtype=np.int64) for t in all_teams}
    for _ in range(n):
        for t, r in _simulate_32(eng, groups, cache).items():
            counts[t][r] += 1
    rows = []
    for t in all_teams:
        c = counts[t]
        rows.append((t, c[1:].sum()/n, c[2:].sum()/n, c[3:].sum()/n,
                     c[4:].sum()/n, c[5]/n))            # P(>=R16,QF,SF,Final,Champ)
    return pd.DataFrame(rows, columns=["team"] + STAGE_NAMES)

