# World Cup kickoff dates -- the strict as-of cutoff for each backtest.
KICKOFF = {2018: pd.Timestamp("2018-06-14"),
           2022: pd.Timestamp("2022-11-20")}

# A neutral-venue base-rate prior (the floor baseline). Home/draw/away.
BASE_RATE = (0.40, 0.28, 0.32)

# -----------------------------------------------------------------------------
# FIT AS OF a date -- everything routes through here so no future data leaks.
# -----------------------------------------------------------------------------
def fit_as_of(date):
    df, as_of = load_and_prepare(as_of=date)
    elo = compute_elo(df)
    _, params = fit_dixon_coles(df, elo)
    return params, elo

# -----------------------------------------------------------------------------
# ACTUAL World Cup matches for a year (pulled straight from results.csv).
# Recorded scores are full-time (90'), so shootout results count as draws --
# which is correct, since the model predicts the 90-minute outcome.
# -----------------------------------------------------------------------------
def get_wc_matches(year, path=None):
    path = path or RESULTS_PATH
    df = pd.read_csv(path, parse_dates=["date"])
    df = normalize_names(df)
    df = df[(df["tournament"] == "FIFA World Cup") & (df["date"].dt.year == year)]
    df = df.dropna(subset=["home_score", "away_score"]).copy()
    return df.reset_index(drop=True)

def outcome(hs, ascore):
    return "H" if hs > ascore else ("A" if ascore > hs else "D")

# -----------------------------------------------------------------------------
# BASELINE: Elo -> 1X2.  Expected score from the Elo gap, a fixed draw band,
# and the rest split by that expected score. Simple and standard.
# -----------------------------------------------------------------------------
def elo_1x2(elo, home, away, neutral, base_draw=0.26):
    rh, ra = elo.get(home, 1500.0), elo.get(away, 1500.0)
    hfa = 0.0 if neutral else HFA_ELO
    E = 1.0 / (1.0 + 10 ** ((ra - rh - hfa) / 400.0))   # expected score for home
    return ((1 - base_draw) * E, base_draw, (1 - base_draw) * (1 - E))

# -----------------------------------------------------------------------------
# SCORE one tournament: model vs Elo-only vs base-rate, with RPS, log-loss,
# and a pooled calibration error.
# -----------------------------------------------------------------------------
def score_tournament(year, kickoff=None, verbose=True):
    cutoff = kickoff or KICKOFF[year]
    params, elo = fit_as_of(cutoff)
    matches = get_wc_matches(year)

    acc = {m: {"rps": [], "ll": []} for m in ("model", "elo", "base")}
    cal = []   # (predicted_prob, hit) pooled over the 3 classes, model only
    skipped = 0
    for r in matches.itertuples(index=False):
        h, a, neu = r.home_team, r.away_team, bool(r.neutral)
        if h not in params["idx"] or a not in params["idx"]:
            skipped += 1
            continue
        o = outcome(r.home_score, r.away_score)

        pr = predict_match(params, h, a, neutral=neu)
        probs = {
            "model": (pr["p_home"], pr["p_draw"], pr["p_away"]),
            "elo":   elo_1x2(elo, h, a, neu),
            "base":  BASE_RATE,
        }
        for name, p in probs.items():
            acc[name]["rps"].append(rps(*p, o))
            acc[name]["ll"].append(log_loss_1x2(*p, o))
        e = {"H": [1, 0, 0], "D": [0, 1, 0], "A": [0, 0, 1]}[o]
        for k in range(3):
            cal.append((probs["model"][k], e[k]))

    summary = pd.DataFrame({
        name: {"RPS": np.mean(v["rps"]), "LogLoss": np.mean(v["ll"])}
        for name, v in acc.items()
    }).T[["RPS", "LogLoss"]]

    ece = _expected_calibration_error(cal)
    if verbose:
        n = len(acc["model"]["rps"])
        print(f"\n=== {year} World Cup backtest (fit as of {cutoff.date()}, "
              f"{n} matches, {skipped} skipped) ===")
        print(summary.to_string(float_format=lambda x: f"{x:.4f}"))
        print(f"model calibration error (ECE): {ece:.3f}   (lower = better)")
        better = summary.loc["model", "RPS"] < summary.loc["elo", "RPS"]
        print("--> model "
              + ("BEATS" if better else "does NOT beat")
              + " the Elo baseline on RPS")
    return summary, ece

def _expected_calibration_error(pairs, bins=10):
    p = np.array([x[0] for x in pairs]); y = np.array([x[1] for x in pairs])
    edges = np.linspace(0, 1, bins + 1)
    ece, n = 0.0, len(p)
    for b in range(bins):
        m = (p >= edges[b]) & (p < edges[b + 1] if b < bins - 1 else p <= edges[b + 1])
        if m.sum():
            ece += (m.sum() / n) * abs(p[m].mean() - y[m].mean())
    return ece

# -----------------------------------------------------------------------------
# HYPERPARAMETER TUNING (e.g. FRIENDLY_WEIGHT).  IMPORTANT: tune on ONE
# tournament, then report final numbers on the OTHER, untouched one -- never
# pick a value using the same World Cup you quote results on.
# -----------------------------------------------------------------------------
def sweep_friendly_weight(tune_year, values=(0.0, 0.1, 0.2, 0.35, 0.5)):
    print(f"Tuning FRIENDLY_WEIGHT on the {tune_year} World Cup "
          f"(do NOT also report on {tune_year}):")
    original = FRIENDLY_WEIGHT
    out = []
    for v in values:
        FRIENDLY_WEIGHT = v
        summ, _ = score_tournament(tune_year, verbose=False)
        out.append((v, summ.loc["model", "RPS"], summ.loc["model", "LogLoss"]))
        print(f"  FRIENDLY_WEIGHT={v:<4}  RPS={out[-1][1]:.4f}  LogLoss={out[-1][2]:.4f}")
    FRIENDLY_WEIGHT = original
    best = min(out, key=lambda r: r[1])
    print(f"  best by RPS: FRIENDLY_WEIGHT={best[0]}")
    return out

def backtest_matches():
    for yr in (2018, 2022):
        score_tournament(yr)
    print("\nRule of thumb: the model should beat base-rate comfortably and "
          "land near (not necessarily beat) Elo. If it trails Elo, revisit "
          "FRIENDLY_WEIGHT / KAPPA via sweep_friendly_weight() on ONE year.")




